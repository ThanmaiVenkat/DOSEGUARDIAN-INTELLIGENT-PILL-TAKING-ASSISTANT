/*
File Name : types.h
Description :
This header file defines user-defined data types used across the
project to improve code readability and portability.

Common typedefs like:
- u8, u16, u32 (unsigned types)
- s8, s16, s32 (signed types)

These types ensure consistent data sizes on ARM architecture.
*/
typedef unsigned char u8;
typedef signed char s8;
typedef signed int s32;
typedef unsigned int u32;
typedef double f64;
typedef float f32;
